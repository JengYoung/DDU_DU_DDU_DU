import Head from 'next/head';
import { Inter } from '@next/font/google';
import styles from '@/styles/Home.module.css';
import React, { MouseEvent, useEffect, useRef, useState } from 'react';
import { v4 as uuidv4 } from 'uuid';

import { openDB, IDBPDatabase } from 'idb';

interface ITodo {
  id: string;
  content: string;
  completed: boolean;
}

export default function Home() {
  const [todos, setTodos] = useState<ITodo[]>([]);

  console.log(todos);
  const [inputValue, setInputValue] = useState('');
  const [todosDB, setTodosDB] = useState<IDBPDatabase | null>(null);

  useEffect(() => {
    async function openDataBase() {
      const db = await openDB('todos', 2, {
        upgrade(db, oldVersion) {
          if (oldVersion < 1) {
            db.createObjectStore('todo', { autoIncrement: true });
          }
          if (oldVersion < 2) {
            db.createObjectStore('garbages', { autoIncrement: true });
          }
        },
      });

      setTodosDB(() => db);

      const tx = db.transaction('todo', 'readwrite');

      const todosData = await tx.store.openCursor(null, 'prev');

      if (!todosData) {
        await tx.store.add([]);
        return;
      }

      setTodos(() => todosData.value);
    }

    openDataBase();
  }, []);

  const updateTodosData = async (nextTodos: ITodo[]) => {
    if (todosDB === null) return;

    const tx = todosDB.transaction('todo', 'readwrite');
    await tx.store.put(nextTodos, 1);
  };

  const onClickButton = async (e: MouseEvent) => {
    e.preventDefault();
    if (inputValue.length === 0) return;

    try {
      const nextTodos = [
        ...todos,
        {
          id: uuidv4(),
          content: inputValue,
          completed: false,
        },
      ];

      await updateTodosData(nextTodos);

      setTodos(() => nextTodos);

      setInputValue(() => '');
    } catch (e) {
      // eslint-disable-next-line
      console.error(e);
    }
  };

  useEffect(() => {
    if (todosDB === null) return;

    const onKeydown = async (e: KeyboardEvent) => {
      const garbagesTx = todosDB.transaction('garbages', 'readwrite');
      const nextTodos = [...todos];

      const value = nextTodos.pop();

      if (e.key === 'z' && e.metaKey && e.shiftKey) {
        const prevTodo = await garbagesTx.store.openCursor(null, 'prev');
        if (!prevTodo) return;

        const restoredTodos = [...todos, prevTodo.value];
        await updateTodosData(restoredTodos);

        const garbagesTx2 = todosDB.transaction('garbages', 'readwrite');
        await garbagesTx2.store.delete(prevTodo.key);
        setTodos(() => restoredTodos);
        return;
      }

      if (e.key === 'z' && e.metaKey) {
        garbagesTx.store.add(value);

        await updateTodosData(nextTodos);
        setTodos(() => nextTodos);
      }
    };

    window.addEventListener('keydown', onKeydown);

    return () => {
      window.removeEventListener('keydown', onKeydown);
    };
  }, [todos, setTodos]);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles['todo-page']}>
        <form className={styles.form}>
          <label htmlFor="input" className={styles.label}>
            <div className={styles['todo__title']}>할 일 입력하기</div>
            <input
              type="text"
              className={styles['todo__input']}
              id="input"
              onChange={(e) => setInputValue(e.target.value)}
              value={inputValue}
            />
          </label>

          <button type="submit" className={styles['todo__button']} onClick={onClickButton}>
            CLICK
          </button>
        </form>

        <ul className={styles['todo-list']}>
          {todos?.map((todo) => (
            <li className={styles['todo-list__item']} key={todo.id}>
              {todo.content}
            </li>
          ))}
        </ul>
      </main>
    </>
  );
}
